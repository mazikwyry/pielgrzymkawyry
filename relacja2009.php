				"Czasem wydaje Ci się, że obok nie ma nikogo. Na dworze deszcz; na stole gazeta, na twarzy pustka. I wtedy wystarczy, że opuścisz swój dom, wyjdziesz na ulice miast, spotkasz kogoś, kto nie jest Ci obcy. I już wiesz, Twoje myśli płyną w określonym kierunku… Ktoś o Tobie pamięta, wystarczy tak niewiele, aby Jego myśli zwróciły się w Twoją stronę..."
<br /><br />
	I czasem tak to właśnie jest, że czegoś nam brakuje w życiu, że czujemy się samotni..
Za oknem pada deszcz.. ale wystarczy, że opuścimy swój dom, spotkamy sąsiadów - ludzi, których znamy i razem wyruszymy w drogę w tym samym kierunku, bo razem raźniej, bo potrzeba tak niewiele, aby być w życiu szczęśliwym! Wyruszymy tam, gdzie wznosi się Jasnogórski szczyt, do Tej, która króluje tam od wieków, do Matki Nieba i Ziemi!
	Maryjo, tak jak trawom, zbożom i kwiatom i nam błogosław i prowadź nas drogą jasną!
