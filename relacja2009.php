<strong>DZIEŃ TRZECI - 20.08.2009 - WOŹNIKI - CZĘSTOCHOWA</strong>

<br /><br />
	 Pjykny poranek... Mszo Świynto, kiero odbyła sie na sali gimnastycznej była jedyno w swoim rodzaju. Kożdy musioł przinieść se stołek i dużo optymizmu na ostatni dzień marszu. Ksiondz Marcin pochaloł nos po głowach i doł krzyżyk na droga. Żar loł sie z nieba, ale to nic, bo to już tego dnia mieli my sie spotkać z Jasnogórską Panią, bo to ona już wkrótce przygarnie nas i przytuli, łzy zetrze z twarzy, a wtedy niebo nad nami pojaśnieje i krzyż będzie lżejszy...<br />
	A nojwiyncyj tego dnia narobili sie nasi fankowi chopcy. Swoimi lizokami stopowali auta coby my cali i zdrowi doszli do celu. Dziynkujemy im za to fest a fest! :D<br />
	Jak w poprzednim roku nie obeszło sie bez listy przebojów. Nojgryfniejszym hitem tyj pielgrzimki okazała się piosynka "Fto Zaufoł Ci". Decyzja Jury była jednogłośno i niepodważalno.<br />
	I wreszcie wleźli my w ostatnio alejka, a naszym ślypiom ukazoł sie cel wędrówki, czyli upragniono Jasna Góra! Cołkie zmęczenie minyło. Dopiero wtedy poczulimy jakbymy sie unosili nad ziemią.<br />
	Wieczorem podrałowali my na Apel Jasnogórski i tam dziynkowali Maryji za wsparcie, bo wcale niełatwo wprost do nieba kroczyć. Życie na ziemi to wędrówka do Boga. Już wiem gdzie jesteś,  nie muszę Cie szukać...	<br />
	Dzień zakończylimy wesołom zabawą z halembską pielgrzimką.
