<strong>DZIEŃ CZWARTY - 21.08.2009 - CZĘSTOCHOWA - WYRY</strong>

<br /><br />
	Bardzo my byli rod, bo naszo familio sie powiynkszyła - dołączyli do nos przyjaciele i rodziny. Wspólnie przeżylimy Droge Krzyżową Pana Jezusa i uczestniczyli we Mszy Świyntyj. Po Koronce do Miłosierdzia Bożego pojechalimy do Wyr, kaj w kościele przywitoł nos farorz i Ksiondz Poloczek. Były podziynkowania, oklaski, śpiyw i radość! Hakuna Matata! :D:D:D
