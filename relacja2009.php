<strong>
DZIEŃ DRUGI - 19.08.2009 - PIEKARY ŚLĄSKIE - WOŹNIKI</strong>
<br /><br />
	Kiedy słonko wstaje i dziyń sie budzi, słychać śpiew Mazika, co raduje ludzi. Noc była ciynżko, boloków kożdemu przybyło, ale cza było zjeść porządne śniodanie i o siły na dalszo droga poprosić Pon Boczka na Mszy Świyntyj.	<br />
	Jak my tak drałowali to Mazik zaczął fandzolić - przeprowadzoł z nami wywiady. Zmęczenie dowało sie poru osobom we znaki, Gosia pedziała, że jom pazury łod śpiywanio bolom:D Niekiere pytania były fest trudne, na przikład: "Czymu Czorno Madonna jest Czorno:D" Postaromy sie tukej rozwiać te wątpliwości bo odpowiedzi były różne. Może bezto, że idymy na Jasno Góra?-tak do kontrastu? A tak naprowda to ponoć na tym świyntym łobrazku zbierała sie sadza z poloncych sie świyc.<br />
	Kedy my tak beblali, łoroz ukazoł sie nom kościoł - piyrszy przystanek drugigo dnia.<br />
Fto był głodny zajadoł  wałówka - klapsznity i biksy z lyjberwusztem, kiere dodały nom skrzideł. Potym my sie ino na chwila zaczimali pod lasem kaj stało sie nieszczynście - naszego Wielebnego użarła osa kiero wyfurgnęła z hasioka. Wszysko dobrze sie skończyło bo nasze szwestery binda mu na rynka zategowały. <br />
	Richtig trudno tak sie nom szło bez tyn las, ale łod czego mo sie kamratów:D Pielgrzimka to jest czas kiedy zbliżomy sie do siebie, kedy jak wyrżniesz ynta to fto inny podo Ci rynka, kedy jesteś spragniony to ftoś Ci do flaszka z wodą, kedy bydzie Ci smutno to ftoś zaśpiewo Ci piosynka. To wszystko sprawio, że pielgrzimkowo atmosfera jest dla nos tako ważno i pozostaje w naszych sercach do nastympnego roku.
	Kedy wyszlimy z lasu to nie była fatamorgana, to były Woźniki:D Nasze nojwiynksze miglance były już w szkole coby być jak nojgibcij wyrychtowane. Niestety nie było tak łatwo wejść do szkoły, bo pani dyrektor se o nos zapomniała i kaś pod palmami leżakowała, na szczynście Matka Woźnikowo nad nami czuwała i w godzina klucze na nos zesłała:D<br />
	 Z głodu tysz my nie padli bo nosz farorzyczek kołoczkami dzielił. Pogodny wieczór był blank pogodny i uśmiechy z gymbów nom nie schodziły, bo kożdy z nos w jakieś zwierze sie przełonaczył. Były wśród nos mućki, koty, ptoki, wieprzki, psy i barany, kiere wspólnie koncert dały.<br />
	Jak co wieczór połączyli my sie w modlitwie śpiywając Apel Jasnogórski, bo... <br />
"jeśli ktoś sie modli, Pan Bóg w nim oddycha"...
