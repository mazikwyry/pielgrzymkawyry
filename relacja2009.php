<strong>DZIEŃ PIERWSZY - 18.08.2009 - WYRY - PIEKARY ŚLĄSKIE</strong>
<br /><br />
	Naszo tegoroczno pielgrzimka rozpoczęła się Mszom Świyntom. Kożdy pielgrzim dostoł plakietka ze swoim imieniem, kiero se przipiął zicherkom do jakli. Po pokropieniu wodom świynconom przez naszego farorza, wyruszylimy na Jasno Góra kaj świynto Marijka już na nos czekała.
	Mazik słowym i piosynkom do drogi zachyncoł i kożdemu pielgrzimowi wiela uwagi poświyncoł. Ogromno radość wszystkich była udziałem, kedy "Fto Zaufoł Ci" śpiewali Bogu na chwałe. Rzykanie i śpiewanie czas na wajzlu skracały i ducha każdego z nos na nowo ubogacały.
	Ksiondz Marcin gryfnie z nami maszeruje i nawet bólu w szwajach nie czuje, a radość serca kiero niesie, pomogo nom nawet w lesie:D
	 I tak my doszli do piyrszego postoju w Kochłowicach kaj my sebuli szczewiki i fuzekle, kiere już w tyn czos szpetnie woniały:D Nasze kiszki marsza grały, bezto gibko żymła z wusztem i kyjzom wszyscy zjodali.<br />
	 Kedy my Zdrowaśki porzykali, ruszylimy w dalszo droga. I tak durch szlimy do nastympnego postoju kaj Pan Prezes i Pan Stasiu już na nos czekali, a my tak piknie dla nich śpiewali: "Krok, krok, za krokiem krok, tchu brak, bo długi szlak, lecz tam u kresu dróg, czeko Pan Prezes, czeko Pan Stasiu(...)":D<br />
	 
	Nieftorym osobom bomble na girach powyskakiwały, ale nasze cudne szwestery
(p.Ania & p.Zdzisia) maści na nie ponakładały i flostry poprzyklejały, ale to nie było ważne... nojważniejsze, że bez te pora dni w roku moglimy być wspólnotom i radować sie pielgrzimowaniem z innymi:D:D:D
	I tak poleku zwlekli my sie na pielgrzimkowy szlak, kiery prowadził nos już furt do Piekor Ślonskich, cołką drogą śpiywajonc "Hakuna Matata"! :D<br />
	Przepełnieni wdzięcznością i radością pokłonilimy sie Matce Piekarskiej. Potym już ino kożdy wahowoł, coby nikt mu nie wlozł w raja pod tusza i do haźlika. <br />
	Droga była ciężko i kożdy czuł sie ufifrany, bezto na pogodny wieczór prziszła ino kapka ludzi, zaś na modlitwie wieczornej było nos fol.
	"Niech nasza droga będzie wspólna, niech nasza modlitwa będzie pokorna, niech nasza miłość będzie potężna, niech nasza nadzieja będzie większa od wszystkiego, co się tej nadziei może sprzeciwić."<br />
	I z tym przesłaniem Jana Pawła II poszlimy słodko nynać na dylówka.
