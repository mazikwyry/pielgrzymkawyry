			<div class="p">
23 Piesza Pielgrzymka Wyry-Częstochowa 2015 przebiegała pod hasłem "3xM, czyli Miłość w trzech odsłonach". Każdego dnia skupialiśmy się na jednej z odsłon 
miłości do: siebie, bliźniego i Boga.
<br><br>
<h2>Dzień 1</h2>
Miłość do siebie samego na pielgrzymce objawia się dbaniem o pierwsze miejsce pod prysznicem, 
zjedzeniem dużej ilości dobrego ciasta, które upiekły wyrskie gospodynie, by uzupełnić brakujące 
kalorie. To wmawianie sobie, że postój już niedługo i nasze ciało odpocznie. Miłość do siebie to 
zawieranie nowych relacji, które tak bardzo nas wzbogacają. Miłość do siebie to trud, który 
podejmujemy, by wzrastać duchowo. To wdzięczność za wszystko, co Bóg mi daje. To przekonanie, że 
dla Boga nie liczy się jak wyglądam, ile mam, bo niezależnie od wszystkiego jestem Jego ukochanym 
dzieckiem.
<br><br>
<h2>Dzień 2</h2>
Miłości do bliźniego chyba najłatwiej doświadczyć właśnie na pielgrzymce. Bliźnich na pielgrzymce 
mieliśmy aż 160 a jednego to nawet zza oceanu. Każdy mimo zmagania się z własnymi trudnościami 
śpieszył pomagać innym. Pomagali fankowi, by nikt nie został w toalecie i z miłości do bliźniego 
zamykali je 10 min przed wyjściem z postoju. Nawet Gwoździu jakiś bardziej miłosierny w tym roku 
był i pozwalał nam odliczać czas do startu;) Pomagali panowie Stasiu, Irek i Adam i podwieźli dzieci, 
by nie zostały z wyczerpania w lesie a do tego mieli ogrom cierpliwości dla zmęczonych drogą 
pielgrzymów. Pomagały panie Ania i Zdzisia, by obolałe nogi mogły dalej iść. Pomagali muzyczni i nie 
żałowali swoich strun głosowych i energii. Pomagał ks. Piotr i dzielił dziwnymi lekami, które działały 
cuda. Pomógł ks. Proboszcz i podarował ciepłe kiełbasy, by strudzeni pielgrzymi mogli się posilić i 
przygrywał nam na akordeonie, by nas rozweselić.
<br><br>
<h2>Dzień 3</h2>
Miłość do Boga objawia się śpiewaniem na cześć Pana, mimo tego, że usta bolą, bo są popękane od 
wiatru. Miłość do Boga przejawią się modlitwą, w tym roku jakże piękną w języku suahili i paru 
innych, które aż trudno rozpoznać. Objawia się pragnieniem dojścia przed tron Maryi i wzruszeniem, 
gdy po dotarciu na Jasną Górę możemy spojrzeć w oczy Matki. To przejście 150 tysięcy kroków, by 
być bliżej nieba.
<br><br>
A doskonała miłość Boga do nas objawia się nieustannie. Nasz Bóg jest wielki!
