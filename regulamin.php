						 <p> &nbsp;&nbsp;W etykiecie znajdziesz kilkanaście propozycji (w zasadzie nie do odrzucenia) zachowań i tradycji pielgrzymkowych.</p>
						 <p>&nbsp;&nbsp;Wszyscy tworzymy atmosferę naszej grupy pielgrzymkowej. To Twój sposób bycia i odnoszenia się do bliźniego odgrywa tutaj ogromną rolę. Z pewnością wysiłkiem będzie okazywanie drugiemu człowiekowi życzliwości, gdy Ty masz pełno pęcherzy, boli Cię kręgosłup, jesteś zmęczony i głodny, a on jest jeszcze w pełni sił. Ale to właśnie w takich sytuacjach ukazuje się nasze człowieczeństwo i prawdziwe oblicze naszego chrześcijaństwa. Pamiętaj, że każde dobro, które będzie Twoim udziałem przyniesie stokrotny plon. </p>
						 <p>&nbsp;&nbsp;Wszystko, co otrzymujemy na pielgrzymce jest darem! Może się zdarzyć, że warunki Twojego noclegu będą inne niż pozostałych pielgrzymów. Nie należy się tym zrażać.. Warto pamiętać, że pielgrzymka ma charakter pokutny, a nie wycieczki orbisowskiej. Zatem wszelkie niedogodności (Np.: spanie w stodole, szkole czy remizie, zimna woda czy też chrapanie innych pielgrzymów) powinny stać się moim wyrzeczeniem, które warto poświęcić konkretnej intencji Np. w tej, dla której podjąłeś trud pielgrzymowania.</p>
						 <p>&nbsp;&nbsp;"Szanuj Przewodnika swego, bo możesz mieć gorszego" - jest to człowiek zabiegany, który jest za wszystko odpowiedzialny. Nie utrudniaj mu życia i nie zadręczaj go zbędnymi pytaniami w stylu: a ile jeszcze do końca, albo czy dzisiaj idziemy piaskami, albo czy dzisiaj będzie świeciło słonko. Przewodnik to nie wróżka. Warto mieć też na względzie to, iż zdanie przewodnika jest ostateczne i najważniejsze. </p>
						 <p>&nbsp;&nbsp; Stosuj się do poleceń służby porządkowej. Nie obrażaj się i nie kłóć się z nimi jeśli będą do znudzenia powtarzać: "łączymy i przyspieszamy", "bardziej na prawo", itp. Do ich powinności należy utrzymanie całej grupy w określonym porządku, pilnowanie równego tempa marszu, bezpieczeństwa na drodze, przestrzegania regulaminu pielgrzymki i kultury bycia wszystkich pielgrzymów. Ze swych obowiązków będą się wywiązywać sumiennie, więc chciej ich zrozumieć. Pod żadnym pozorem nie przechodź w trakcie marszu i na postoju na drugą stronę jezdni - nie ryzykuj swojego i cudzego życia.    </p>
						 <p>&nbsp;&nbsp;Ze służby medycznej korzysta się w uzasadnionej potrzebie. Najlepiej na postoju. Jeśli np. obtarłeś sobie nogę, to od razu nie umieraj. Zgłoś się do punktu medycznego na najbliższym postoju. Pamiętaj również, że ich jest kilka, a pielgrzymów kilkudziesięciu. Poza tym warto byś pamiętał o tym, że do punktu medycznego, w miejscu noclegu pielgrzym przychodzi zaraz po umyciu się, nie czekając do późnego wieczora. Pielęgniarki także potrzebują snu.  </p>
						 <p>&nbsp;&nbsp;Może się zdarzyć, że pielęgniarka zabroni Ci iść dalej i wyśle Cię do samochodu. Należy wówczas unikać dwóch zachowań:</p>
					<ul><li> Ukrywania tego i dalszego marszu. Pamiętaj, konsekwencje tego mogą być poważne i okaże się, że będziesz musiał wracać do domu,
					<li> Nadmiernego wykorzystywania transportu samochodowego, choćby kosztem symulowania i oszustwa. Wynikiem takiego działania jest także odesłanie do domu. </ul>

						 <p>&nbsp;&nbsp;Punktualność to podstawa! Będę mógł Ci tylko współczuć, jeśli się spóźnisz i będziesz musiał gonić całą grupę. Grupa nie będzie na Ciebie czekała, a jazda autostopem w trakcie pielgrzymki jest obca każdemu pielgrzymowi.</p>
						 <p>&nbsp;&nbsp;Istnieje prawdopodobieństwo, że zostaniesz wyznaczony do niesienia na jednym z etapów tuby lub wzmacniacza. Nie wymiguj się od tego - to jest twój dar dla grupy. Nie stwarzaj powodów do interwencji porządkowych. </p>
