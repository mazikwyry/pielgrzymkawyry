            W 1993 roku ks. Jerzy Słota, ówczesny wikariusz, napisał nowy rozdział w historii wyrskich wypraw pątniczych. 
            Pierwszy raz pielgrzymka wyruszyła spod kościoła w Wyrach i trasą przez Piekary Śląskie i Woźniki doszła do Częstochowy.
