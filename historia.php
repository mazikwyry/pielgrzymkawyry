			<div class="p">
            Za polami, za lasami, w sąsiedniej wsi Gostyń na przełomie lat 70/80 rodziła się wyrska tradycja pielgrzymkowa. 
            Parafianie z Wyr początkowo doczepiali się do pielgrzymki szlakiem Orlich Gniazd (Jura Krakowsko-Częstochowska) organizowanej przez parafian z Gostyni. 
            Z upływem lat było nas coraz więcej, aż w końcu znalazła się grupa, która zorganizowała pierwszą pielgrzymkę pieszą, która rozpoczęła się w Wyrach (jesteśmy w trakcie ustalania kiedy dokładnie to było).
            Do roku 1992 pielgrzymka odbywała się przez Jurę Krakowsko-Częstochowską. Pielgrzymka zaczynała się w Zawierciu, gdzie pielgrzymi dojeżdżali pociągiem.
