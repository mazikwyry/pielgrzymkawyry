       <div style="width:100%;  float:left;  " class="header">
        <div>Spotkanie popielgrzymkowe!</div>
      </div>

      Już w przyszły piątek, czyli <strong>7 września 2012</strong> w kościele parafialnym w Wyrach oraz w salkach odbędzie się spotkanie popielgrzymkowe.
      <br><br>
      Rozpocznie się ono o <strong>19.00 od Mszy Św. w intencji naszej wspólnoty pielgrzymkowej oraz wszystkich sponsorów tegorocznej pielgrzymki.</strong>
      Następnie udamy się do salek parafialnych gdzie będzie czas na wspólne wspomnienia. Będzie kawa, herbata, ciastko (można też w miarę możliwości i chęci dorzucić jakąś paczkę ciastek od siebie).
      <br><br>
      W planie mamy premierę relacji video z tegorocznej pielgrzymki oraz prawdziwą gratkę - kilka materiałów archiwalnych, zdjęć i filmów z pielgrzymek 1994-2004.
      Dlatego <strong>zapraszamy wszystkich, którzy kiedykolwiek pielgrzymowali z naszą pielgrzymką i wszystkich, którzy czują się z nią związani!</strong>
      Oczywiście po premierze te zdjęcia i filmy pojawią się w internecie, na naszej stronie, także jeżeli ktoś nie będzie mógł uczestniczyć w naszym spotkaniu zapewniam, że to go nie ominie :D
      Będzie też okazja poznać naszego nowego przewodnika pielgrzymkowego - ks. Adam Kiedrzyna!
      <br><br>
      Serdecznie już teraz wszystkich zapraszam! Jest to okazja, żeby znów wspólnie się spotkać i <strong>podziękować Bogu za ten wspaniały czas,</strong>
      który nam ofiarował podczas pielgrzymki, za wszystkie wyproszone łaski.
      Oczywiście nie zabraknie też wspólnego śpiewu i zabawy:D
      <br><br>
      Zaproszeni są wszyscy - razem z rodzinami i wszystkimi, którzy są zainteresowani.
      Mamy nadzieję, że w wyrskim kościele znowu zrobi się ŻÓŁTO! (wiecie o co chodzi :D)



      <br><br>

      <div style="width:100%;  float:left;  " class="header">
        <div>Doszliśmy!</div>
      </div>
      żółta20 dobiegła końca, ale ta niesamowita atmosfera, którą stworzyło ponad 110 żółtków zostanie w nas na długo. Zostaną też w nas i będą wciąż wzrastać owoce tego pielgrzymowania, bo jak to trafnie zauważył ks. Adam Skutela w kazaniu - "Nikt nie wróci z pielgrzymki takim, jakim był zanim na nią wyruszył. Każdego ona zmieni!". My idziemy, ofiarujemy trud, ból, a Bóg robi swoje! Już wkrótce na stronie pojawią się relacje, zdjęcia i inne materiały, dlatego zachęcamy do częstego odwiedzania i polubienia nas na facebooku, dzięki czemu będziecie na bieżąco. Zapraszamy również do podzielenia się wrażeniami i/lub świadectwem z pielgrzymki, tych którzy z nami maszerowali &ndash; <a href="ksiega.php"><strong>pielgrzymkowa księga wrażeń 2012</strong></a>.
      <br><br>

      <br>
      <div style="width:100%;  float:left;  " class="header">
        <div>Ruszyliśmy!</div>
      </div>
      Pielgrzymka w drodze! 22 sierpnia wchodzimy na Jasną Górę, a 23 obchodzimy "Żółty dzień" u Pani Jasnogórskiej. 
      O godz. 6.50 wyruszy z parkingu przy kościele autokarowa pielgrzymka do Częstochowy. W Częstochowie w Kaplicy Cudownego Obrazu o godz. 9.30 zostanie odprawiona Msza św. w int. naszej wspólnoty parafialnej.
      Po Mszy św. ok godz. 10.45 odprawimy na wałach jasnogórskich Drogę Krzyżową. Naszą pielgrzymkę zakończymy  Koronką do Bożego Miłosierdzia o godz. 15.00. Powrót planowany jest na godz. 17.30.

      <br><br>
      <br>
      <div style="width:100%;  float:left;  " class="header">
        <div>Zmiany - ważne!</div>
      </div>
      Przygotowania idą pełną parą - nie zapomnijmy, że najważniejszym elementem przygotowania jest modlitwa! 
      <br>
      Informujemy o ważnej zmianie. W tym roku, z powodów organizacyjnych pielgrzymka rozpocznie się <strong>krótkim nabożeństwem o godzinie 6.30</strong>, po którym od razu wyruszymy w drogę - najpóźniej o 7.00.  Takie rozwiązanie ma wiele zalet. Przede wszystkim będziemy wcześniej na miejscu, ominie nas największy upał (na drugim postoju będzie Msza Św.) i wiele innych. Dlatego prosimy wszystkich o <strong>załadowanie bagaży na pakę do 6.30</strong>. Przy "pace" rozdawane będą identyfikatory i śpiewniki oraz można tam będzie dokonać wpłaty za pielgrzymkę! Tych, którzy planują zakup bułek po nabożeństwie informujemy, że Piekarnia w Wyrach sprzedaje bułki całą noc i ranek - wystarczy podejść z tyłu - prosimy uczynić to przed nabożeństwem :D  
      <br><br>
      <div style="width:100%;  float:left;  " class="header">
        <div>Tuba, koszty i inne...</div>
      </div>
      Mamy nową tubę pielgrzymkową <strong>LDM Trumpet 70</strong> (tzn. już niedługo przywiezie ją kurier)! Z tej okazji składamy serdeczne Bóg zapłać wszystkim ofiarodawcą, którzy okazali swoje wielkie serce i troskę o życie parafii. Gazetki w wakacje osiągały kosmiczne ceny, jak na kartkę A4. Obiecujemy modlitwę za wszystkich, którzy wrzucili do koszyka choć kilka groszy i pomogli nam zakupić tubę!
      <br><br>
      Podane zostały koszty pielgrzymki &ndash; w tym roku (tak jak w poprzednim) trzeba będzie wyłożyć <strong>75zł</strong>. Jeżeli kogoś nie stać, oczywiście nie musi się odpisywać - wystarczy poinformować o tym księdza Piotra, a środki na pewno się znajdą. W koszty wpisują się opłaty za 3 noclegi, ubezpieczenie i autokar powrotny (najdroższa część). Opłatę najlepiej wnieść, w miarę możliwości, przez pielgrzymką w zakrystii, ale oczywiście można także zapłacić w trakcie pielgrzymki. Prosimy o zgodne kwoty (worek pięciozłotówek nie jest zbyt poręczny).
      <br><br>
      Na liście na dzień dzisiejszy mamy <strong>106 osób</strong>. Jeżeli ktoś zapisał się, a nie będzie mógł pójść, nawet jeśli dowie się o tym w ostatniej chwili, <strong>prosimy nas o tym poinformować</strong> (najlepiej mailem)! To dla nas bardzo ważne.
      <br><br>
      Internet to narzędzie zawodne, dlatego osoby, które zapisały się przez Internet, a nie dostały w odpowiedzi od nas maila z treścią "Zapisy przyjęte", proszone są o ponowny kontakt na mail <a href="mailto:pielgrzymka.wyry@gmail.com">pielgrzymka.wyry@gmail.com</a>. Dotąd nie było takich problemów, ale lepiej dmuchać na zimne!


      <br><br>

      <div style="width:100%;  float:left;  " class="header">
        <div>Zapisy nadal otwarte!</div>
      </div>
      <br><br>
      Mimo, że zapisy w zakrystii dzisiaj "oficjalnie" zostały zamknięte, nie oznacza, że nikogo już ze sobą nie weźniemy. Do dziś zapisało się ok. <strong>80 osób! Nadal można się zapisywać.</strong> W pewnym jednak momencie, gdy zostaną załatwione autobusy pojawi się informacja, o ilości wolnych miejsc w autobusach powrotnych. Chodzi o to, żeby nie okazało się, że zapiszą się np. 302 osoby (łącznie z pielgrzymami autokarowymi) i jeden autobus pojedzie prawie pusty a koszty takiej operacji są duże. Wielu pielgrzymów ma możliwość wrócić samochodem z rodziną, więc tak to "wycichtujemy", żeby było dobrze. Już niedługo podamy informacje co do kosztów (walczymy o jak najmniejszy wymiar kary :D). Czasu zostało już mało, więc nie czekaj - zapisz się!
      <br><br>
